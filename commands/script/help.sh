MINIX has a help facility called 'man'.  It provides online manual pages
for commands, system calls, and library routines.  It can be set up to
provide information about other subjects as well by adding files to the
/usr/man directory.  To get help about a particular command, type:

	man

You will be given a display showing all the commands for which online
information is available.  To select one, use the arrows to highlight the
one you are interested in, and then hit the RETURN key.  After viewing the
man entry, hit RETURN again to return to the menu.  To exit the menu, hit the
q (quit) hit.  Since the screen is too small to show all the items available,
use the arrows to move below the bottom of the page, and more entries will
scroll up and become visible.  The Home key returns to the start of the list.

To can also get information about a specific entry by listing that entry as an
argument to man, for example, to learn more about cc, type:

	man cc

To learn more about the man command itself, type:

	man man

END
